int MgDecaScoreCalc(int gameNo, int mgScore)
{
    MGDECASCORE *scoreP;
    HuVec2f *point;
    HuVec2f *pointNext;
    int pointNo;
    s32 mgNo;
    float value;
    int result;
    result = 1000;
    OSReport(lbl_802BF859, mgScore);
    mgNo = -1;
    switch(gameNo) {
        case 0:
            if(mgScore == 0) {
                result = 0;
            } else {
                value = mgScore/50.0f;
                mgNo = 502;
            }
            break;

        case 1:
            if(mgScore == 0) {
                result = 0;
            } else {
                value = mgScore/50.0f;
                mgNo = 504;
            }
            break;

        case 2:
            value = mgScore;
            mgNo = 563;
            break;

        case 3:
            value = mgScore;
            mgNo = 506;
            break;

        case 4:
            if(mgScore == 0) {
                result = 0;
            } else {
                value = mgScore/1000.0f;
                mgNo = 507;
            }
            break;

        case 5:
            value = mgScore;
            mgNo = 510;
            break;

        case 6:
            if(mgScore == 0) {
                result = 0;
            } else {
                value = mgScore/50.0f;
                mgNo = 511;
            }
            break;

        case 8:
            if(mgScore == 0) {
                result = 0;
            } else {
                value = mgScore/50.0f;
                mgNo = 512;
            }
            break;

        case 7:
            if(mgScore == 0) {
                result = 0;
            } else {
                value = mgScore/50.0f;
                mgNo = 513;
            }
            break;

        case 9:
            value = mgScore/100.0f;
            mgNo = 514;
            break;
    }
    if(mgNo > 0) {

        int i;
        for(scoreP=&MgDecaScoreTbl[0], i=DECA_SCORE_NUM; i--; scoreP++) {
            if(scoreP->mgNo == mgNo) {
                break;
            }
        }
        point = NULL;
        pointNext = &scoreP->points[0];
        result = 1000;
        if(scoreP->type != DECA_SCORE_TIME) {
            for(pointNo=0; pointNo<scoreP->pointNum; point=pointNext, pointNext++, pointNo++) {
                if(value <= pointNext->y) {
                    if(!point) {
                        result = 0;
                        break;
                    } else {
                        result = point->x;
                        result += (pointNext->x-point->x)*((value-point->y)/(pointNext->y-point->y));
                        break;
                    }
                }

            }
        } else {
            for(pointNo=0; pointNo<scoreP->pointNum; point=pointNext, pointNext++, pointNo++) {
                if(value >= pointNext->y) {
                    if(!point) {
                        result = 0;
                        break;
                    } else {
                        result = point->x;
                        result += (pointNext->x-point->x)*((point->y-value)/(point->y-pointNext->y));
                        break;
                    }
                }
            }
        }
    }
    if(result < 0) {
        result = 0;
    } else if(result > 1000) {
        result = 1000;
    }
    return result;
}

